let num1=-5.5;
console.log(Math.abs(num1));
// bằng 5.5 vì trị tuyệt đối thì mất âm thành dương
let num2=10;
console.log(Math.abs(num2));
// bằng 10 vì trị tuyệt đối 10 vẫn là 10



let num3=4.4;
console.log(Math.ceil(num3));
// bằng 5 vì ceil nó làm tròn lên trên nên nó bằng 5
let num4=5.8;
console.log(Math.ceil(num4));
// bằng 6 vì ceil nó làm tròn lên trên nên nó bằng 6



let num5=5.7;
console.log(Math.floor(num5));
// bằng 6 vì floor nó làm tròn xuống trên nên nó bằng 5
let num6=6.1;
console.log(Math.floor(num6));
// bằng 6 vì floor nó làm tròn xuống trên nên nó bằng 6



let num7=10.1;
console.log(Math.round(num7));
// bằng 10 vì round nó làm tròn gần nhất nên nó bằng 10
let num8=20.6;
console.log(Math.round(num8));
// bằng 21 vì round nó làm tròn gần nhất nên nó bằng 21